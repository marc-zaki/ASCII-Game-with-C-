void moveHero(int& rhero, int& chero, char move, char game[][350], int& scrollCstart, int& scrollCend, int& scrollRstart, int& scrollRend)
{
	if (move == 'w')
	{
		rhero -= 2;
		scrollRend -= 2;
		scrollRstart-=2;
	}

	if (move == 'a')
	{
		chero--;
		scrollCstart--;
		scrollCend--;
	}
	if (move == 's' && game[rhero+1][chero] != '=')
	{
		rhero++;
		scrollRstart++;
		scrollRend++;
	}
	
	if (move == 'd')
	{
		chero += 2;
		scrollCend++;
		scrollCstart++;
	}
	
	
}
